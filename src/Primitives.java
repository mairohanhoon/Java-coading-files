public class Primitives {
    public static void main(String[] args) {
        // Primitive is a data type which you can't break any further
         int rollno = 64; // 4 bytes
         char letter = 'r'; //
         // Character starts with single quotes '' but the string starts with double quotes ""
        float marks = 98.47f; // for decimal values // 4 bytes
        double largeDecimalNumbers = 234978235235.24356234; // 8 bytes
        long largeInteger = 235246245324123L; // 8 bytes
        boolean check = true; // for true false cases




         // String is not primitive
         String name = "Rohan";
    }
}
